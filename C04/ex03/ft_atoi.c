#include <unistd.h>

int	ft_atoi(char *str)
{
	int	i;
	int	isaret;
	int	toplam;

	i = 0;
	isaret = 1;
	toplam = 0;
	while ((str[i] >= 9 && str[i] <= 13) || str[i] == ' ')
		i++;
	while (str[i] == '+' || str[i] == '-')
	{
		if (str[i] == '-')
			isaret *= -1;
		i++;
	}
	while (str[i] >= '0' && str[i] <= '9')
	{
		toplam = (str[i] - '0') + (toplam * 10);
		i++;
	}
	return (toplam * isaret);
}
